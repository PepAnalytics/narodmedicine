class Command(BaseCommand):
    help = (
        "Загрузка начальных данных "
        "(50 болезней, 100 симптомов, 100 ингредиентов, 200 методов)."
    )

    def add_arguments(self, parser) -> None:
        parser.add_argument(
            "--reset",
            action="store_true",
            help="Удалить существующие данные доменной модели перед загрузкой.",
        )

    @transaction.atomic
    def handle(self, *args, **options) -> None:  # noqa: ANN002, ANN003
        if options["reset"]:
            self.stdout.write("Удаление существующих доменных данных...")
            UserRating.objects.all().delete()
            RemedyIngredient.objects.all().delete()
            Remedy.objects.all().delete()
            DiseaseSymptom.objects.all().delete()
            Disease.objects.all().delete()
            Symptom.objects.all().delete()
            Ingredient.objects.all().delete()
            EvidenceLevel.objects.all().delete()

        evidence_map = self._load_evidence_levels()
        symptoms = self._load_symptoms()
        ingredients = self._load_ingredients()
        diseases = self._load_diseases()
        self._link_diseases_and_symptoms(diseases, symptoms)
        self._load_remedies(diseases, ingredients, evidence_map)

        self.stdout.write(
            self.style.SUCCESS(
                "Загрузка завершена: "
                f"{Disease.objects.count()} болезней, "
                f"{Symptom.objects.count()} симптомов, "
                f"{Ingredient.objects.count()} ингредиентов, "
                f"{Remedy.objects.count()} методов."
            )
        )

    def _load_evidence_levels(self) -> dict[str, EvidenceLevel]:
        evidence_map: dict[str, EvidenceLevel] = {}
        for payload in EVIDENCE_LEVELS:
            level, _ = EvidenceLevel.objects.update_or_create(
                code=payload["code"],
                defaults={
                    "description": payload["description"],
                    "color": payload["color"],
                    "rank": payload["rank"],
                },
            )
            evidence_map[level.code] = level
        return evidence_map

    def _load_symptoms(self) -> list[Symptom]:
        symptom_objects: list[Symptom] = []
        for name in SYMPTOM_NAMES:
            symptom, _ = Symptom.objects.update_or_create(
                name=name,
                defaults={
                    "description": (
                        f"Симптом «{name}». Требуется клиническая интерпретация "
                        "с учётом сопутствующих факторов."
                    )
                },
            )
            symptom_objects.append(symptom)
        return symptom_objects

    def _load_ingredients(self) -> list[Ingredient]:
        ingredient_objects: list[Ingredient] = []
        for name in INGREDIENT_NAMES:
            ingredient, _ = Ingredient.objects.update_or_create(
                name=name,
                defaults={
                    "description": f"Натуральный ингредиент: {name}.",
                    "contraindications": (
                        "Индивидуальная непереносимость, склонность к аллергическим "
                        "реакциям, беременность или лактация без консультации врача."
                    ),
                },
            )
            ingredient_objects.append(ingredient)
        return ingredient_objects

    def _load_diseases(self) -> list[Disease]:
        disease_objects: list[Disease] = []
        for name, description in DISEASE_DATA:
            disease, _ = Disease.objects.update_or_create(
                name=name,
                defaults={"description": description},
            )
            disease_objects.append(disease)
        return disease_objects

    def _link_diseases_and_symptoms(
        self,
        diseases: list[Disease],
        symptoms: list[Symptom],
    ) -> None:
        randomizer = random.Random(20260319)
        symptom_usage: dict[int, int] = {symptom.id: 0 for symptom in symptoms}

        for index, disease in enumerate(diseases):
            selected_symptom_ids = {
                symptoms[index % len(symptoms)].id,
                symptoms[(index * 2 + 7) % len(symptoms)].id,
            }
            while len(selected_symptom_ids) < 6:
                selected_symptom_ids.add(randomizer.choice(symptoms).id)

            for symptom_id in selected_symptom_ids:
                weight = round(randomizer.uniform(0.6, 3.0), 2)
                DiseaseSymptom.objects.update_or_create(
                    disease=disease,
                    symptom_id=symptom_id,
                    defaults={"weight": weight},
                )
                symptom_usage[symptom_id] += 1

        for symptom in symptoms:
            if symptom_usage[symptom.id] > 0:
                continue
            fallback_disease = randomizer.choice(diseases)
            DiseaseSymptom.objects.update_or_create(
                disease=fallback_disease,
                symptom=symptom,
                defaults={"weight": round(randomizer.uniform(0.8, 2.2), 2)},
            )

    def _load_remedies(
        self,
        diseases: list[Disease],
        ingredients: list[Ingredient],
        evidence_map: dict[str, EvidenceLevel],
    ) -> None:
        randomizer = random.Random(20260320)
        evidence_cycle = ["A", "B", "C", "D", "E"]

        for disease_index, disease in enumerate(diseases):
            for variant_index, variant in enumerate(REMEDY_VARIANTS):
                evidence_code = evidence_cycle[(disease_index + variant_index) % 5]
                evidence_level = evidence_map[evidence_code]
                remedy_name = f"{variant['title']} при {disease.name.lower()}"

                remedy, _ = Remedy.objects.update_or_create(
                    disease=disease,
                    name=remedy_name,
                    defaults={
                        "description": (
                            "Поддерживающий народный метод для симптоматического "
                            f"облегчения при состоянии «{disease.name}»."
                        ),
                        "recipe": variant["recipe"],
                        "risks": variant["risks"],
                        "source": self._build_source_url(disease.name, variant_index),
                        "evidence_level": evidence_level,
                    },
                )

                selected_ingredients = randomizer.sample(ingredients, k=3)
                remedy.remedy_ingredients.all().delete()
                for amount_index, ingredient in enumerate(selected_ingredients):
                    RemedyIngredient.objects.create(
                        remedy=remedy,
                        ingredient=ingredient,
                        amount=AMOUNTS[(variant_index + amount_index) % len(AMOUNTS)],
                    )

    @staticmethod
    def _build_source_url(disease_name: str, variant_index: int) -> str:
        disease_slug = slugify(disease_name, allow_unicode=True)
        return f"https://example.org/folk/{disease_slug}-{variant_index + 1}"
